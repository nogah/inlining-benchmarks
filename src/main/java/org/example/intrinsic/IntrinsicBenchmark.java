package org.example.intrinsic;

import java.util.concurrent.TimeUnit;

import org.openjdk.jmh.annotations.Benchmark;
import org.openjdk.jmh.annotations.BenchmarkMode;
import org.openjdk.jmh.annotations.Fork;
import org.openjdk.jmh.annotations.Measurement;
import org.openjdk.jmh.annotations.Mode;
import org.openjdk.jmh.annotations.OutputTimeUnit;
import org.openjdk.jmh.annotations.Scope;
import org.openjdk.jmh.annotations.State;
import org.openjdk.jmh.annotations.Warmup;

@BenchmarkMode(Mode.AverageTime)
@OutputTimeUnit(TimeUnit.NANOSECONDS)
@Warmup(iterations = 5, time = 1, timeUnit = TimeUnit.SECONDS)
@Measurement(iterations = 10, time = 1, timeUnit = TimeUnit.SECONDS)
@Fork(value = 1, jvmArgsAppend = {
        "-XX:+UnlockDiagnosticVMOptions",
        "-XX:+TraceClassLoading",
        "-XX:+LogCompilation",
        "-XX:CompileCommand=print,*IntrinsicBenchmark.*",
        "-XX:CompileCommand=print,*Integer.bitCount"
})
@State(Scope.Thread)
public class IntrinsicBenchmark {

    int x = 73;

    @Benchmark
    public int intrinsicBitCount() {
        return Integer.bitCount(x);
    }

    @Benchmark
    public int manualBitCount() {
        return bitCount(x);
    }

    // Identical to the code in java.lang.Integer.bitCount(int i);
    private int bitCount(int i) {
        i = i - ((i >>> 1) & 0x55555555);
        i = (i & 0x33333333) + ((i >>> 2) & 0x33333333);
        i = (i + (i >>> 4)) & 0x0f0f0f0f;
        i = i + (i >>> 8);
        i = i + (i >>> 16);
        return i & 0x3f;
    }

}
